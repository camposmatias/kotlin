fun main() {
    saludo()
    bienvenida()
    despedida()
}

fun saludo() {
    println("\nHola. Me llamo Araucaria, soy residente de los bosques patagónicos.")
}

fun bienvenida() {
    println("Te doy la binevenida, te daremos fuego, cobijo y alimento.")
}

fun despedida() {
    println("...pero cuídame, usándome de manera responsable y sustentable con el medioambiente.")
}
